package com.ushulabubko;

public class Loop {
    public static void main(String[] args) {

        int count = 16;


        while (count > 0) {                                 // here i state that as long as the variable count is higher than 0 the following function will execute, if not the program will conitue
            System.out.println(count);                      // her it prints the current value in the count variable
            count--;                                        // here it subtracts 1 from the value stored in the variable count
        }

    }




}
